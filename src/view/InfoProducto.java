    private void botSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botSaveActionPerformed
        //pasar datos a registrar datos
        
        tipoInventario.show(botSave, WIDTH, WIDTH);
        
        //actionlistener
        cocina.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e) {
                botSave.setText("Cocina");
                labelEstado.setText("Guardando en inventario de cocina");
                //conversion
                String name = textNombre.getText();
                int ide = Integer.parseInt(textID.getText());
                Float pre = Float.parseFloat(textPrecio.getText());
                
                
                //registrar en cocina
                nuevo = new NuevoProducto();
                nuevo.saveCocina(ide, name, pre);
            }
        });
        
        sala.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e) {
                botSave.setText("Sala");
                labelEstado.setText("Guardando en inventario de sala");
                
                //conversion
                String name = textNombre.getText();
                int ide = Integer.parseInt(textID.getText());
                Float pre = Float.parseFloat(textPrecio.getText());
                
                //registrar en Sala
                nuevo = new NuevoProducto();
                nuevo.saveSala(ide, name, pre);
            }
        });
        
        
        
    }//GEN-LAST:event_botSaveActionPerformed

